;====================================================================
; RESET and INTERRUPT VECTORS
;====================================================================
; Reset Vector
ORG  0000h
JMP MAIN
ORG 030h
MAIN: 
MOV P2,#00h
CLR A

LOOP: 
          DA A
          MOV P2,A 
          ADD a,#01h
          CALLl Delay
JMP LOOP

Delay:
          MOV R2,#0ffh
          wait2: MOV R1,#0ffh
          wait1: 
          NOP 
          NOP 
          NOP
          DJNZ R1, wait1
          DJNZ R2, wait2
RET
END
